inline uint32_t reverse(uint32_t x)
{
    x = (x & 0x55555555) <<  1 | ((x >>  1) & 0x55555555);
    x = (x & 0x33333333) <<  2 | ((x >>  2) & 0x33333333);
    x = (x & 0x0f0f0f0f) <<  4 | ((x >>  4) & 0x0f0f0f0f);
    x = (x << 24) | ((x & 0xff00) << 8) | ((x >> 8) & 0xff00) | (x >> 24);
    return x;
}

