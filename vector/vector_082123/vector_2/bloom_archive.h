inline uint32_t shuffle(uint32_t x)
{
    uint32_t t = 0;
    t = (x ^ (x >> 8)) & 0x0000ff00; x = x ^ t ^ (t << 8);
    t = (x ^ (x >> 4)) & 0x00f000f0; x = x ^ t ^ (t << 4);
    t = (x ^ (x >> 2)) & 0x0c0c0c0c; x = x ^ t ^ (t << 2);
    t = (x ^ (x >> 1)) & 0x22222222; x = x ^ t ^ (t << 1);
    return t;
}

inline uint32_t unshuffle(uint32_t x)
{
    uint32_t t = 0;
    t = (x ^ (x >> 1)) & 0x22222222; x = x ^ t ^ (t << 1);
    t = (x ^ (x >> 2)) & 0x0c0c0c0c; x = x ^ t ^ (t << 2);
    t = (x ^ (x >> 4)) & 0x00f000f0; x = x ^ t ^ (t << 4);
    t = (x ^ (x >> 8)) & 0x0000ff00; x = x ^ t ^ (t << 8);
    return t;
}

