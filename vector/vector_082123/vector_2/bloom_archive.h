    //inline static uint64_t _hash_1(uint64_t value) { return value * (value > 4000 ? 1 : BIG_PRIMES[value & 0xf]) + 386809; }
    inline static uint64_t _hash_2(uint64_t value) { return reverse(uint32_t(value))/* * BIG_PRIMES[value & 0xf]*/; }

